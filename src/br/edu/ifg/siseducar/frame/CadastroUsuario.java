    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        
        String nome = jTextFieldNomeCadUsuario.getText();
        String login = jTextFieldLoginCadUsuario.getText();
        String senha = jPasswordFieldCadUsuario.getText();
        String confSenha = jPasswordFieldConfCadUsuario.getText();
        
        if ("".equals(nome.trim())) {
            JOptionPane.showMessageDialog(rootPane, "Por favor informe um nome!", "Aviso!", JOptionPane.WARNING_MESSAGE);
            jTextFieldNomeCadUsuario.requestFocus();
            return;
        }
        
        if ("".equals(login.trim())) {
            JOptionPane.showMessageDialog(rootPane, "Por favor informe um usuario!", "Aviso!", JOptionPane.WARNING_MESSAGE);
            jTextFieldLoginCadUsuario.requestFocus();
            return;
        }
        
        if ("".equals(senha.trim())) {
            JOptionPane.showMessageDialog(rootPane, "Por favor informe uma senha!", "Aviso!", JOptionPane.WARNING_MESSAGE);
            jPasswordFieldCadUsuario.requestFocus();
            return;
        }
        
        if(!senha.equals(confSenha)){
            JOptionPane.showMessageDialog(rootPane, "Senhas diferentes, favor confirmar a senha!", "Aviso!", JOptionPane.WARNING_MESSAGE);
            jPasswordFieldCadUsuario.setText("");
            jPasswordFieldConfCadUsuario.setText("");
            jPasswordFieldCadUsuario.requestFocus();
            return;
        }
        
        //Sql para inserir os valores recebidos
        String sql = "INSERT INTO usuario VALUES ("
                + "DEFAULT, "
                + "'" + nome.toLowerCase() + "', "
                + "'" + login.toLowerCase() + "', "
                + "'" + senha + "');";
        System.out.println(sql);
        try {
            Statement stm = Main.conexao.createStatement();
            stm.executeUpdate(sql);
        } catch (SQLException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }        

        limparCampos();
        JOptionPane.showMessageDialog(rootPane, nome + " adicionado com sucesso!");
    }//GEN-LAST:event_jButtonSalvarActionPerformed

